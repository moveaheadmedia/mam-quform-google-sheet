declare( strict_types = 1 );

namespace QuformToGoogleSheet\Common\Abstracts;

use QuformToGoogleSheet\Config\Plugin;

/**
 * The Base class which can be extended by other classes to load in default methods
 *
 * @package QuformToGoogleSheet\Common\Abstracts
 * @since 1.0.0
 */
abstract class Base {
	/**
	 * @var array : will be filled with data from the plugin config class
	 * @see Plugin
	 */
	protected $plugin = [];

	/**
	 * Base constructor.
	 *
	 * @since 1.0.0
	 */
	public function __construct() {
		$this->plugin = Plugin::init();
	}
}
